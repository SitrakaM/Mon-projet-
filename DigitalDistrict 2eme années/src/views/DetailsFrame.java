    public void specificValue(int id){
        ResultSet rs=Model.Afficher.getById(id);
        try {
            int ide=0;
            while(rs.next()){
                    anarana.setText("ANARANA: "+rs.getString(2));
                    fanampiny.setText("FANAMPINY: "+rs.getString(3));
                    datenaissance.setText("TERAKA TAMIN'NY: "+rs.getString(4));
                    lieunaissance.setText("TAO: "+rs.getString(5));
                    cin.setText("KARA-PANONDRO: "+rs.getString(7));
                    profession.setText("ASA AMAN-DRAHARAHA: "+rs.getString(8));
                    nationalite.setText("ZOM-PIRENENA: "+rs.getString(10));
                    logement.setText("MONINA AO AMIN'NY: "+rs.getString(6));
                    identifiant.setText("FOKONOLONA LAHARANA FAHA:"+rs.getInt(1));
                   ImageIcon ii3;
                ii3 = new ImageIcon("C:\\DigitalDistrict\\[Ataovy ato ny sary]\\"+rs.getString(11));
//NOI18N
                    Image img=(ii3).getImage().getScaledInstance(photo.getWidth(), photo.getHeight(), Image.SCALE_SMOOTH);
                    ii3=new ImageIcon(img);
                    photo.setIcon(ii3);
                    ide=rs.getInt(1);
            }
            rs=Model.Afficher.getPere(ide);
                while(rs.next()){
                    nompere.setText("RAY: "+rs.getString("Nom"));
                    profpere.setText("ASANY: "+rs.getString("Prof_pere"));
                }
                
            rs=Model.Afficher.getMere(ide);
                while(rs.next()){
                    nomere.setText("RENY: "+rs.getString("Nom"));
                    profmere.setText("ASANY: "+rs.getString("Prof_mere"));
                }
            this.setVisible(true);
            this.setLocationRelativeTo(null);
        } catch (SQLException ex) {
            Logger.getLogger(DetailsFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
